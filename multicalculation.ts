
export class Rectangle
{
     rectangle(length1:number,bredth:number)
     {
         let ar=length1*bredth;
         console.log("Area of rectangle is "+ar);
     }
}
export class Square
{
     square(side:number)
     {
         let ar=side*side;
         console.log("Area of square is "+ar);
     }
}
export class Circle
{
     circle(radius:number)
     {
         let ar=3.14*radius*radius;
         console.log("Area of circle is "+ar);
     }

}